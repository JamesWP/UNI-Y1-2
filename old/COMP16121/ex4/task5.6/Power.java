public class Power
{
  public static void main(String[] args)
  {
    int base = Integer.parseInt(args[0]);
    int exponent = Integer.parseInt(args[1]);
    int result = base;
    for(int count = 1; count < exponent;count++)
      result *= base;

    System.out.println(base + "^" + exponent + " = " + result);
  }
}
